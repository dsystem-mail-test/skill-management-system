# ==============================
# マスキング
# ==============================
def mask_text(text):
    text = re.sub(r'\S+@\S+', '', text)
    text = re.sub(r'\d{2,4}-\d{2,4}-\d{3,4}', '', text)
    text = re.sub(r'株式会社\S+', '', text)
    return text


# ==============================
# JSON抽出
# ==============================
def extract_json(text):
    match = re.search(r'\{.*\}', text, re.DOTALL)
    if match:
        return match.group(0)
    return "{}"


